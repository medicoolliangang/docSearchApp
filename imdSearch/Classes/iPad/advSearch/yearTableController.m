#pragma mark - View lifecycle

- (void)viewDidLoad
{
    [super viewDidLoad];
    
    yearData =[[NSMutableArray alloc] initWithCapacity:20];
    
    for(int i = 0;i < 24;i++)
    {
        if(i==0)
            [yearData addObject:@"无限制"];
        else
            [yearData addObject:[NSString stringWithFormat:@"%d",2015-i]];
        
    }
}

